package min.edu;

public class Coffee_Switch {
	
	public static void  output(int n) {
		
		switch (n) {
		case 0:
			
			System.out.println("아메리카노가 출력되었습니다.");
			
			break;
			
		case 1:
			
			System.out.println("라떼가 출력되었습니다.");
			
			break;
			
		case 2:
			
			System.out.println("마끼야또가 출력되었습니다.");
			
			break;
			
		case 3:
			
			System.out.println("카푸치노가 출력되었습니다.");
			
			break;

		default:
			System.out.println("없는 메뉴 입니다.");
			break;
		}
		
		
		
		
		
		
	}

}
