/*
명명법
1. 파스칼(pascal) : 대문자로 시작 소문자 쓰다가 의미있는 글이 나오면 대문자( 클래스명 )
2. 헝가리안(hungarian): 소문자로 시작 하다가 의미있는 글이 나오면 대문자( 변수명 , 메소드명 )
3. 카멜(camel): 헝가리안 하고 같음 , 그러나 약어를 사용할때 소문자를 사용해 주세요 
4. 소문자(lower): 전체를 다 소문자로 작성( 예약어(public static void) , 패키지명 )
5. 대문자(Upper): 전체를 대문자로 작성(상수) ex Math.PI 같이 Math는 클래스명이고 PI는 상수
*/
public class Naming_Rule {
	
	public static void main(String[] args) {
		System.out.println(Math.PI);
	}
	
	

}


